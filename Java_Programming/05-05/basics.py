# Use of Logic Operators and Bitwise Operators
# Logical operators: and, or, not
# Bitwise operators: &, |, ^, ~, <<, >>

print((3>1) and (4>2)) # True and True = True
print((3>1) & (4>2)) # True and True = True
print(7 & 10) # 7 = 0111, 10 = 1010, 7 & 10 = 0010 = 2
print(9 and 8) # True and True = True(8: last value)
print(0 and 8) # False and True = 0
print(bin(22))